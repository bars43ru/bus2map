// Package egts реализует парсинг протокола EGTS (Единая ГЛОНАСС/GPS Транспортная Система).
// Протокол стандартизирован ГОСТ Р 54619-2011 и используется для передачи телеметрии.
// Поддерживает передачу данных о местоположении, скорости, курсе и других параметрах транспортных средств.
package egts

import (
	"time"
)

// Point представляет точку GPS в формате протокола EGTS.
// Содержит информацию о местоположении, скорости и курсе транспортного средства.
type Point struct {
	PacketID  uint32    // id пакета данных (PID)
	Client    uint32    // id устройства (OID)
	Time      time.Time // дата и время сообщения в формате UTC
	Latitude  float64   // широта в градусах (WGS84)
	Longitude float64   // долгота в градусах (WGS84)
	Speed     uint16    // скорость в км/ч
	Course    uint8     // курс в градусах (0-359)
}
