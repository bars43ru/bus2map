// Package xslog предоставляет расширенные функции для работы с логгером slog.
// Содержит утилиты для форматирования и обработки логов.
package xslog

import "log/slog"

// Error создает атрибут лога для ошибки.
// Используется для единообразного форматирования ошибок в логах.
//
// Параметры:
//   - err: ошибка для логирования
//
// Возвращает:
//   - slog.Attr: атрибут лога с текстом ошибки
func Error(err error) slog.Attr {
	return slog.String("error", err.Error())
}
